void setup() {
    Serial.begin(115200);
    WiFi.begin(WIFI_SSID, WIFI_PASSWORD);
    while (WiFi.status() != WL_CONNECTED) {
        delay(500);
        Serial.print(".");
    }
    Serial.println("WiFi connected");

    if (!initCamera()) {
        Serial.println("Camera init failed");
        while (true) {
            delay(1000);
        }
    }
}

